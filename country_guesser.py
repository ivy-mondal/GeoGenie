def country_guess(image_path):
    def encode_image(image_path):
        with open(image_path, "rb") as image_file:
            return base64.b64encode(image_file.read()).decode('utf-8')

    base64_image = encode_image(image_path)
    api_key = open(".env", mode="r").read()
    client = OpenAI(api_key=api_key)
    completion = client.chat.completions.create(
        model="gpt-4o",
        messages=[
            {"role": "system", "content": "Your goal is to guess in which the given image was taken, give only country name,nothing else"},
            {
                "role": "user",
                "content": [
                    {
                        "type": "text",
                        "text": "Which country is this image from?"
                    },
                    {
                        "type": "image_url",
                        "image_url": {
                            "url": f"data:image/jpeg;base64,{base64_image}"
                        }
                    }
                ]
            }
        ]
    )
    guess = completion.choices[0].message.content
    if guess in countries:
        return guess
    else:
        return "United States"


